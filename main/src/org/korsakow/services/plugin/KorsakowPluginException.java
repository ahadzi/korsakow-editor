package org.korsakow.services.plugin;

public class KorsakowPluginException extends Exception
{
	public KorsakowPluginException() {
		super();
	}
	public KorsakowPluginException(String message, Throwable cause) {
		super(message, cause);
	}
	public KorsakowPluginException(String message) {
		super(message);
	}
	public KorsakowPluginException(Throwable cause) {
		super(cause);
	}
}
